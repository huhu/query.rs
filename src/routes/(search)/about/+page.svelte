  <h2 class="pb-6">About Query.rs</h2>
  <p>
    Query.rs is a static search engine for Rust. Static means all search indexes
    are built in advance and loaded into memory, making it blazingly fast.
  </p>
  <p class="py-4"></p>
  <p>
    Query.rs was inspired by Rust Search Extension. Due to the partial support
    of the
    <a
      href="https://developer.chrome.com/docs/extensions/reference/api/omnibox"
    >
      omnibox API
    </a>
    by some browsers, Rust Search Extension is not available on browsers such as
    <a href="https://github.com/huhu/rust-search-extension/issues/87">Safari</a
    >,
    <a href="https://github.com/huhu/rust-search-extension/issues/10">Vivaldi</a
    >,
    <a href="https://github.com/huhu/rust-search-extension/issues/230">Arc</a>,
    etc. Query.rs addresses this gap.
  </p>
